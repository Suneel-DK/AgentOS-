# в”Җв”Җ Preflight в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
preflight() {
    mkdir -p /var/log/agentos
    log "=== AgentOS First Boot Started ==="
    log "Hostname: $(hostname)"
    log "IP: $(hostname -I | awk '{print $1}' || echo 'not yet assigned')"

    # Ensure scripts are executable
    chmod +x "${AGENTOS_DIR}/scripts/"*.sh
}

# в”Җв”Җ Wait for network в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# The install scripts need internet access (Ollama, pip)
wait_for_network() {
    log "вҸі Waiting for network..."
    local attempts=0
    until ping -c 1 -W 2 8.8.8.8 > /dev/null 2>&1; do
        sleep 3
        attempts=$((attempts + 1))
        if [ "${attempts}" -ge 20 ]; then
            log "вҡ   Network not available after 60s вҖ” proceeding anyway (offline installs will fail)"
            return
        fi
    done
    log "вң… Network ready"
}

# в”Җв”Җ GPU setup в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
setup_gpu() {
    log "--- GPU Detection ---"
    bash "${AGENTOS_DIR}/scripts/detect-gpu.sh" || {
        log "вҡ   GPU detection encountered an error вҖ” continuing without GPU"
    }
}

